    private void checkoutButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkoutButtonActionPerformed

        if (!ward.getText().equals("")) {

            JDialog jDialog = new JDialog();
            CheckOut checkOut = new CheckOut(nameNew,phoneNew,provinceNew,districtNew,wardNew,noteText.getText());
            jDialog.setSize(650, 700);
            checkOut.setBounds(0, 0, 650, 700);
            jDialog.setUndecorated(true);
            jDialog.add(checkOut);
            jDialog.setLocationRelativeTo(null);

            checkOut.getcancelButton().addActionListener((ActionEvent e) -> {
                jDialog.dispose();
            });

            jDialog.setModal(true);
            jDialog.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(null, "Please import address");
        }

    }//GEN-LAST:event_checkoutButtonActionPerformed


